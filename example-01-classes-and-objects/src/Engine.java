public class Engine {
	private String name;
	private double torque;
	private int power;
	private double weight;
	private double moveableEffort;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getTorque() {
		return torque;
	}
	public void setTorque(double torque) {
		this.torque = torque;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public double getMoveableEffort() {
		return moveableEffort;
	}
	public void setMoveableEffort(double moveableEffort) {
		this.moveableEffort = moveableEffort;
	}
}
